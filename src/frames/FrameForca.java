    private void btnJogarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnJogarActionPerformed
        // TODO add your handling code here:
        lblInfo.setVisible(false);
        if (erros >= 6 || btnJogar.getText().equalsIgnoreCase("NOVO JOGO")) {
            iniciarForca();
        } else {
            if (txtLetra.getText().equals("")) {
                lblInfo.setVisible(true);
                lblInfo.setText("Digite uma letra!");
                txtLetra.requestFocus();
            } else {
                forca = "";

                for (int i = 0; i < palavra.length(); i++) {
                    //Acertou letra
                    if (txtLetra.getText().equalsIgnoreCase(palavra.substring(i, i + 1))) {
                        palavraArray[i] = txtLetra.getText();
                        errou = 'n';
                    }
                }

                for (int i = 0; i < palavra.length(); i++) {
                    if (palavraArray[i].equalsIgnoreCase("*")) {
                        forca += "_" + " ";
                    } else {
                        forca += palavraArray[i] + " ";
                    }
                }
                lblForca.setText(forca);
                forca = "";
                

                if (errou == 's') {
                    erros++;
                    if(!lblLetras.getText().contains(txtLetra.getText())) {
                        lblLetras.setVisible(true);
                        lblLetras.setText(lblLetras.getText() + txtLetra.getText() + " ");
                    }
                    switch (erros) {
                        case 1:
                            lblDesenho.setIcon(new ImageIcon(getClass().getResource("01.png")));
                            break;
                        case 2:
                            lblDesenho.setIcon(new ImageIcon(getClass().getResource("02.png")));
                            break;
                        case 3:
                            lblDesenho.setIcon(new ImageIcon(getClass().getResource("03.png")));
                            break;
                        case 4:
                            lblDesenho.setIcon(new ImageIcon(getClass().getResource("04.png")));
                            break;
                        case 5:
                            lblDesenho.setIcon(new ImageIcon(getClass().getResource("05.png")));
                            break;
                        default:
                            lblDesenho.setIcon(new ImageIcon(getClass().getResource("06.png")));
                            break;
                    }
                }
                
                txtLetra.setText("");
                txtLetra.requestFocus();
                
                errou = 's';

                if (erros == 6) {
                    lblInfo.setVisible(true);
                    lblInfo.setText("VOCÊ PERDEU!!");
                    txtLetra.setEnabled(false);
                    btnJogar.setText("NOVO JOGO");
                }
            }

        }
        palavraJogador = "";
        for (int i = 0; i < palavraArray.length; i++) {
            palavraJogador += palavraArray[i];
        }
        System.out.println("Palavra jogador: " + palavraJogador);
        System.out.println("Palavra: " + palavra);
        if (palavraJogador.equals(palavra)) {
            lblInfo.setVisible(true);
            lblInfo.setText("VOCÊ VENCEU!!");
            txtLetra.setEnabled(false);
            btnJogar.setText("NOVO JOGO");
        }
    }//GEN-LAST:event_btnJogarActionPerformed

