    //Gerar uma nova palavra e imprimir os traços
    public void iniciarForca() {
        lblInfo.setVisible(false);
        lblLetras.setText("");
        lblDesenho.setIcon(new ImageIcon(getClass().getResource("forca.png")));
        palavra = palavras[rn.nextInt(palavras.length)].toUpperCase();
        System.out.println(palavra);
        txtLetra.setEnabled(true);
        btnJogar.setText("JOGAR");
        txtLetra.requestFocus();
        errou = 's';
        erros = 0;
        palavraArray = new String[palavra.length()];
        forca = "";

        //Imprimir a forca com os tracinhos pela primeira vez
        for (int i = 0; i < palavra.length(); i++) {
            letra = palavra.substring(i, i + 1);
            if (!(letra.equals("-") || letra.equals(" "))) {
                palavraArray[i] = "*";
                forca += "_" + " ";
            } else {
                palavraArray[i] = letra;
                forca += letra + " ";
            }
        }
        lblForca.setText(forca);
        forca = "";
    }

    //Apagando uma segunda letra digitada e convertendo a letra em maiúsculo
    public void apagarLetra() {
        if (txtLetra.getText().length() > 0) {
            char letra = txtLetra.getText().charAt(0);
            txtLetra.setText((letra + "").toUpperCase());
        }
    }

